double HarmonicOscillator3D_0::eval(const Walker* walker, int i) {
    
    (void) walker;
    (void) i;

    //exp(-k^2*r^2/2)
    
    H = 1;
    return H*(*exp_factor);
    
}

double dell_HarmonicOscillator3D_0_x::eval(const Walker* walker, int i) {

    x = walker->r(i, 0);
    
    //-k^2*x*exp(-k^2*r^2/2)
    
    H = -(*k2)*x;
    return H*(*exp_factor);
    
}

double dell_HarmonicOscillator3D_0_y::eval(const Walker* walker, int i) {

    y = walker->r(i, 1);
    
    //-k^2*y*exp(-k^2*r^2/2)
    
    H = -(*k2)*y;
    return H*(*exp_factor);
    
}

double dell_HarmonicOscillator3D_0_z::eval(const Walker* walker, int i) {

    z = walker->r(i, 2);
    
    //-k^2*z*exp(-k^2*r^2/2)
    
    H = -(*k2)*z;
    return H*(*exp_factor);
    
}

double lapl_HarmonicOscillator3D_0::eval(const Walker* walker, int i) {
    
    //k^2*(k^2*r^2 - 3)*exp(-k^2*r^2/2)
    
    H = (*k2)*((*k2)*walker->get_r_i2(i) - 3);
    return H*(*exp_factor);
    
}

