/*
    Subclass Eval functions
*/


double HarmonicOscillator_0::eval(const Walker* walker, int i) {
    
    //exp(-k^2*r^2/2)
    
    H = 1;
    return H*(*exp_factor);
    
}

double dell_HarmonicOscillator_0_x::eval(const Walker* walker, int i) {

    x = walker->r(i, 0);
    
    //-k^2*x*exp(-k^2*r^2/2)
    
    H = -(*k2)*x;
    return H*(*exp_factor);
    
}

double dell_HarmonicOscillator_0_y::eval(const Walker* walker, int i) {

    y = walker->r(i, 1);
    
    //-k^2*y*exp(-k^2*r^2/2)
    
    H = -(*k2)*y;
    return H*(*exp_factor);
    
}

double lapl_HarmonicOscillator_0::eval(const Walker* walker, int i) {
    
    //k^2*(k^2*r^2 - 2)*exp(-k^2*r^2/2)
    
    H = (*k2)*((*k2)*walker->get_r_i2(i) - 2);
    return H*(*exp_factor);
    
}

